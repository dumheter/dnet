#include "dnet_assert.hpp"
#include <cstdlib>
#include <iostream>

namespace dnet {

void dnet_assert(const bool predicate, const std::string& message) {
  if (!predicate) {
    std::cerr << "Assertion failed with message [" << message << "]."
              << std::endl; /* force a flush */
    std::exit(-1);
  }
}

}  // namespace dnet
